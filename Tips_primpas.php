			 <h1>Primeros Pasos</h1>

<p>
La información que presentas tiene que ser clara, concisa y lógica, que no lleve a errores y confusiones a quien la lee. Los responsables de selección no invierten más 10 segundos en escanear y decidir si leen o no tu Currículum Vitae, y unos 30 segundos en leerlo y tomar la decisión de invitar al candidato para una entrevista. NO LO OLVIDES!!

Antes de redactar y enviar el Currículum Vitae tienes que plantearte una serie de preguntas. Escribe las respuestas y después ya habrá tiempo de dar forma al Currículum Vitae:
<ul type="circle">
<li>¿Qué creo que busca la empresa y /o el seleccionador? 

<li>	¿Qué habilidades son las que necesita alguien desarrollar y aplicar en el puesto?

<li>	¿Qué logros y funciones en anteriores puestos he desempeñado y me pueden ayudar a realizar bien mi nuevo trabajo?

<li>	¿Tengo la preparación académica correcta? He actualizado mis conocimientos?

<li>	¿Hablo idiomas? 

<li>	¿Tengo otras experiencias en la vida que me han ayudado a desarrollar habilidades y capacidades y a resolver problemas? (Por ejemplo viajando al extranjero solo, ayudando en alguna ONG, tomando responsabilidades en la universidad…)

<li>	¿Qué tipo de puesto y/o es el ideal para mi? Me gusta trabajar sólo o en equipo? Soy creativo o prefiero que me dirijan en el proyecto?

<li>	¿Qué me ha gustado de mi puesto anterior? Qué aspectos o funciones no quiero desarrollar de nuevo?

<li>	¿Razones por las que cambio o busco un nuevo trabajo? Se siempre honesto en las respuestas, no hay que mentir, pero tampoco es necesario dar todos los detalles o hablar despectivamente de la empresa y los compañeros/jefes anteriores.

<li>	¿Hay periodos de inactividad en mi trayectoria profesional? ¿Cómo los puedo explicar positivamente? ¿Qué he aprendido y qué habilidades he utilizado por ejemplo viajando, cuidando a mis hijos , buscando empleo durante mucho tiempo …?
<p>
<h1>Organiza las respuestas por apartados, por ejemplo: </h1>

<li>	¿Cual es mi objetivo? ¿Qué nivel de responsabilidad busco?

<li>	Resumen de habilidades, capacidades y conocimientos adquiridos.

<li>	Experiencia.

<li>	Estudios / formación.

<li>	Uso de programas informáticos / Internet.

<li>	Idiomas.

<li>	Puntos de interés que quiero destacar: aficiones, intereses, voluntariado.

<p><hr><p>Cuando escribas el Curriulum tienes que tener presente lo que quieres comunicar: la información que contiene tiene que ser relevante para conseguir lo primero una entrevista de trabajo y así tener la posibilidad de que te conozcan y puedas demostrar que eres el candidato idóneo para el puesto de trabajo ofertado.<p>

Sólo hay que reflejar los intereses y aficiones si tienen relación con el puesto al que optas; por ejemplo, si te gusta leer, y el puesto es en un medio de comunicación o por ejemplo te gusta los deportes o la decoración y optas por un puesto en una compañía de deportes o de decoración. Si eres voluntario y ayudas a la gente pero optas a un puesto de ventas, es posible que el seleccionador piense que no vas a resistir lo duro y competitivo que es un puesto comercial , donde no siempre prima la persona, sino las ventas y los objetivos.

