    		<center><h1> La apariencia es lo primero que se ve...</h1></center>
<p>Causar una buena impresión (imagen que se proyecta o actitud corporal), es por lo menos, no desagradar visualmente al entrevistador. Se ha comprobado que el 55 % de lo que se transmite en un proceso de comunicación humana, se relaciona con el lenguaje no verbal, con lo "que no se dice con palabras" y sólo el 45 % con el lenguaje verbal, es decir, causar una primera buena impresión.<p>
    	<h1>La presentación personal</h1>
<b>Para hombres:</b><p> traje o conjunto de blazer y pantalón. Colores: Gris topo y azul, los llamados colores "de autoridad". Clásicos y sobrios, difícilmente queden mal. Ahora sí, es importante el color de la camisa. Esos colores de traje, combinan con camisa blanca (color neutro) y una corbata al tono. El color de las medias debe hacer juego con ella. Es importante el lustrado de los zapatos, es un detalle muy observado. ¡Ojo!<p>Nada de jeans o prendas "casual". Las joyas (anillos con piedras, pulseras, etc.), en general, no causan buena impresión si las lleva un varón. Aún hay empresas donde las barbas o el pelo largo, no están bien vistas. Esta "norma" no figura escrita en ningún lado, por lo cual, si no quieres afeitarte la barba o cortarte el pelo, al menos, llevalos cuidados. Si vas a "cara limpia" que sea impecablemente afeitado.<p><p><hr>
<b>Para mujeres:</b><p> Tailleur clásico o "faldas", en lugar de pantalones, con colores agradablemente combinados. Se recomienda poca bijouterie y un maquillaje sobrio. Tacos no demasiado elevados y un peinado que no llame la atención. Si vas perfumada, que no sea exagerado (piensa también que puede haber luego otra entrevista laboral). Escotes pronunciados, minis "infartantes", blusas demasiado ajustadas, están contraindicadas para una entrevista laboral.<p>   
