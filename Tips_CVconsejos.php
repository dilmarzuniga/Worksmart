                <h1>Durante Entrevista Consejo 2</h1>  
<h3>

Crea una buena impresión. Las primeras impresiones son de gran importancia. Muchos empresarios consideran que tras haber visto entrar por la puerta a un candidato, observar la forma de dar la mano y sentarse, son capaces de decidir sobre su valía</h3>
<ul type="circle">

<li>Saluda al entrevistador con una fórmula convencional: Buenas tardes Sr. Ruiz
<li>Siéntate derecho/a, ni al borde de la silla (inseguridad), ni repantingado/a (falta de respeto)
<li>No seas el/la primero/a en extender la mano
<li>Saluda con un apretón de manos y sonríe, mirándole a los ojos
<li>No te sientes hasta que te lo digan
<li>No tutees si no te lo indican
<li>Estáte atento/a y simpático/a
<li>Evita ponerte nervioso/a y los gestos que lo indiquen como morderte las uñas, golpear con el boli en la mesa, agitarte en el asiento...
<li>No fumes
<li>No interrumpas
<li>Mira al entrevistador a los ojos, pero sin intimidar
<li>Deja que él tome la iniciativa
<li>No hables demasiado, ni demasiado poco
<li>Cuida tu lenguaje
<li>No peques de agresivo/a ni de sensiblón/a
<li>No te aproximes mucho a él
<li>No pongas los codos encima de la mesa del entrevistador
<li>No cruces los brazos, puede parecer que estás a la defensiva
<li>Si te ofrecen una bebida puedes aceptar pero, que no sea alcohólica            
        </ul>


<h1>Piensa antes de contestar</h1>
<ul>
<li>Responde clara y brevemente
<li>Di siempre la verdad
<li>Si te preguntan sobre ti mismo/a, enfatiza tu formación, experiencia y resultado de tus trabajos
<li>No uses palabras rebuscadas
<li>No respondas con evasivas o dudas, ni con monosílabos
<li>No esperes demasiado tiempo para responder, podría parecer síntoma de distracción
<li>No utilices expresiones tajantes "siempre, nunca", ni utilices latiguillos "osea", "este", "bueno"...
<li>Muestra entusiasmo por el trabajo, pero no lo supliques
<li>No hables mal de las empresas en las que has trabajado
<li>No te niegues a responder preguntas
<li>Si te hacen preguntas de tipo cómo: si va de viaje con su novio/a ¿ qué piden una o dos habitaciones?; responde que en tu opinión ese asunto es irrelevante para deducir tu idoneidad para el puesto al que optas. Dilo con seriedad pero no con agresividad. Estas preguntas valoran tu control emocional
</ul>

<h1>Piensa positivamente</h1>

<li>No pidas el puesto como si fuera una limosna
<li>No digas que necesitas terriblemente el trabajo
<li>No critiques a pasados empresarios
<li>Ejemplo: No digas, "acabo de terminar mis estudios y no tengo experiencia profesional" enfocado de otra manera la respuesta correcta sería "estoy disponible para trabajar y dispuesto/a a poner en práctica todos los conocimientos que he adquirido en mis estudios
Haz preguntas

		
