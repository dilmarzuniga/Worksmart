    		 <h1>Que es la carta de recomendacion</h1>
			 <p>
			 La carta debe invitar a quien la recibe a tomar una decisión, pero sin sentir que se lo está obligando a ello. Y además, no debe sonar a que se está pidiendo un favor al potencial contratante, sino que se está ofreciendo una verdadera oportunidad de solución a los problemas de la empresa a la que se destina la carta. 

Para ello es bueno mantenerse lejos de los “slogans” o de las frases de oferta conocidas. Es mejor relatar algún caso real o enumerar a clientes que han tenido experiencias exitosas con el servicio ofrecido.

Como se señaló antes, es necesario causar una buena impresión en quien lee la carta de presentación de los servicios, pero para ello no se debe ofrecer nada que no se pueda llevar a cabo o servicios que aún no estén disponibles (aunque estén en los planes). De ser contratado, cada promesa puede ser exigida y si no se puede cumplir con ella, la relación habrá empezado con el pie izquierdo.

Usted y nosotros.<p> Hay quienes recomiendan que en la carta de presentación de servicios a una empresa se hable sobre todo desde el lado del potencial cliente. Es decir, que no se utilicen tanto las frases “nuestros productos” o “nosotros ofrecemos”, sino “usted tiene a disposición”, “su empresa encontrará”, ectétera.

De esa forma, la sensación de cercanía o de relación entre ambas empresas será más estrecha, ya que cada una de las necesidades que tenga quien recibe la carta de presentación de los servicios puede ser solucionada por quien la escribe. 
		
