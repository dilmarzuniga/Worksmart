    		  <h1>Tipos de Curriculum. Curriculum Cronol&oacute;gico </h1>
    		  <p>&nbsp;</p>
    		  <p class="primero_tips"> Este tipo de curr&iacute;culum, como su propio nombre indica, es aquel que organiza la informaci&oacute;n cronol&oacute;gicamente, partiendo de los logros m&aacute;s antiguos y llegando hasta los m&aacute;s recientes. Esto permite al selector de personal conocer la evoluci&oacute;n ascendente de tu carrera profesional, aunque no siempre facilita el objetivo fundamental de cualquier curriculum: conseguir una entrevista personal.</p>
    		  <p class="primero_tips">El punto positivo de este tipo de organizaci&oacute;n es que pone de relieve, si cabe, la estabilidad y la evoluci&oacute;n ascendente de tu carrera.&nbsp;<strong>Destaca la promoci&oacute;n, el aumento de tareas o las subidas en las escala profesional.</strong><br />
    		    No obstante, tambi&eacute;n deja patente a simple vista si has tenido alg&uacute;n periodo de inactividad o si has sufrido cambios frecuentes del lugar de trabajo. Esto obliga frecuentemente a dar muchas explicaciones o incluso a rellenar los huecos con informaci&oacute;n que no tiene importancia para el trabajo al que te presentas.</p>
    		  <p class="primero_tips">Por ello, este tipo es recomendable si dispones de una buena experiencia laboral, no has cambiado demasiado de trabajo, ni tienes grandes periodos de tiempo en los que no has trabajado. Tambi&eacute;n es interesante si te conviene destacar alguna empresa reconocida en la que hayas trabajado.</p>
    		  <p class="primero_tips">El curr&iacute;culum cronol&oacute;gico tambi&eacute;n puede presentarse en el orden inverso, es decir, destacando en primer lugar los &uacute;ltimos logros y la ocupaci&oacute;n m&aacute;s reciente, para ir despu&eacute;s avanzando en la experiencia anterior. Es una presentaci&oacute;n menos tradicional, pero que cada vez la prefieren m&aacute;s empresas. La principal ventaja es que resalta las experiencias m&aacute;s recientes que indudablemente son las que m&aacute;s interesan a quienes te van a contratar.</p>
    		  <h2> Ventajas del curr&iacute;culum cronol&oacute;gico </h2>
