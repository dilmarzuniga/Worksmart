    		  <h1>Carta para Presentar a una Empresa</h1><p>
			  Ya sea contratando a alguien o simplemente tomando la guía telefónica, se debe crear una base de datos de los destinatarios de la carta de presentación. 

En caso de que se envíen a empresas, es bueno obtener el nombre de la persona que debería leerla, para dirigirla directamente y establecer una comunicación más directa. <p>Evitar el encabezado “A quien corresponda” ya es una ganancia en el camino hacia la relación comercial que se busca.

Por su puesto que el formato a emplear en la redacción tiene que ser el de la carta formal, y es bueno imprimirla en un papel de buena calidad, además de chequear las veces que sea necesario que la ortografía y sintaxis de la carta que se utiliza para presentar a la empresa sea excelente.

En cuanto al contenido, la carta de presentación de una empresa tiene que dejar en claro desde el principio qué es lo que se está ofreciendo y cuáles son las características que la distinguen de la competencia. <p>

Estos puntos son centrales, aunque no hay que hacer un ensayo de cientos de páginas, sino todo lo contrario. Lo ideal es que la información se concentre en una página y se deje la información de contacto, para continuar esa comunicación luego.

Aquí hay algunos consejos rápidos para la redacción de la carta de presentación: 
<ul type="circle">
<li>Los expertos recomiendan que el texto se organice en cuatro bloques: 

<li> Saludo personal o general. 
<li> Descripción de la empresa, sus servicios y productos; su historia (breve), misión y objetivo.
<li> Saludo y agradecimiento por el tiempo destinado a la lectura.



 

<li> Firma con información de contacto (correo electrónico, teléfono y dirección). En caso de que se utilice una hoja membretada estos datos podrán obviarse en la firma, pero deben estar claramente impresos en algún lugar de la carta de presentación de una empresa.
<li>El texto debe ser corto y lo recomendable es que una carta para presentar una empresa no supere los cuatro o cinco párrafos.

<li>El texto debe ser fluido, debe atrapar el interés de quien lo lee. Para esto es importante ir directamente a lo que se ofrece. La empresa que se presenta con esta carta debe responder a alguna necesidad de su potencial cliente, por lo tanto, empezar por ese lugar es lo mejor.

<li> Escribir en positivo es otra recomendación de los expertos. Las cartas de presentación no deben incluir negativas, el texto será siempre una demostración de lo que se puede y lo que se tiene, no de lo demás. Pero es importante recordar: no se deben prometer servicios o productos que no se tienen, la sinceridad es clave en una buena relación comercial.

<li> La prolijidad de la presentación en la carta será también un valor de la empresa. Por lo tanto, se debe cuidar al extremo que el papel sea adecuado, que el logo se vea correctamente y por sobre todo, que los datos de contacto figuren claros, de modo que si se alcanzó la meta de interesar a quien la lee, la comunicación posterior sea exitosa.
 

		
