    		  <h1>Tipos de Curriculum. Curriculum Funcional </h1>
    		  <p>&nbsp;</p>
    		  <p class="primero_tips">A diferencia del curr&iacute;culum cronol&oacute;gico, el funcional distribuye la informaci&oacute;n por temas. De esta forma, permite proporcionar un conocimiento r&aacute;pido de tu formaci&oacute;n y experiencia en un &aacute;mbito determinado. Adem&aacute;s, al no seguir una progresi&oacute;n cronol&oacute;gica,&nbsp;<strong>permite seleccionar los puntos positivos, destacar las habilidades propias y logros conseguidos, al tiempo que se omiten o difuminan los no deseados</strong>: errores de recorrido, periodos de paro, cambios frecuentes de trabajo sin haber promocionado&hellip;</p>
    		  <p class="primero_tips">Por todo ello, el modelo funciona es conveniente cuando se quiere acceder a un sector profesional totalmente diferente, para lo cual no es relevante tu experiencia laboral anterior, si no tus habilidades, capacidades y tu forma de trabajar. Escoge este tipo de curr&iacute;culum si, por ejemplo, llevas tiempo sin trabajar y vas a volver a incorporate al mundo laboral, si acabas de terminar las carrera y no tienes apenas experiencia que acreditar o si cambias frecuentemente de puesto de trabajo. Tambi&eacute;n es el m&aacute;s adecuado si has trabajado como empresario, aut&oacute;nomo o freelance.</p>
    		  <h2> Ventajas del curr&iacute;culum funcional: </h2>
    		  <div id="lista_tips">Se centra en tus capacidades y habilidades, antes que en tus circunstancias laborales.</div>
    		  <div id="lista_tips">Permite mucha m&aacute;s flexibilidad y libertad en la organizaci&oacute;n de la informaci&oacute;n de tus logros y habilidas, y adem&aacute;s facilita la inclusi&oacute;n de otra informaci&oacute;n relacionada como intereses o motivaciones.</div>
    		          <div id="lista_tips">Es especialmente &uacute;til para usarse con la nuevas tecnolog&iacute;as.</div>
  		       
    		    <h2>Inconvenientes del curr&iacute;culum funcional: </h2>
    		          <div id="lista_tips">No resalta el nombre de las empresas para las que has trabajado ni el tiempo que has estado en cada lugar de trabajo.</div>
    		          <div id="lista_tips">Limita la descripci&oacute;n del puesto y sus responsabilidades.</div>
  		         
